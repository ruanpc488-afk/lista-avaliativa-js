let n1 = 8;
let n2 = 6;
let media = (n1 + n2) / 2;

console.log("Sua média foi: " + media);

if (media == 10) {
    console.log("Desempenho Excelente");
} else if (media >= 7) {
    console.log("Aprovado");
} else if (media >= 5) {
    console.log("Recuperação");
} else {
    console.log("Reprovado");
}
